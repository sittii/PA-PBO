    private void showData() {
        try {
            st = conn.createStatement();
            rs = st.executeQuery("SELECT p.kodepinjam, b.kodebuku, b.judulbuku, p.statuspinjam, p.tglpinjam, p.tglkembali FROM buku AS b JOIN Peminjamanbuku AS p ON b.kodebuku = p.buku_kodebuku WHERE p.anggota_kodeuser = '" + kodeuser + "' AND statuspinjam = 'dipinjam'");

            int no = 1;

            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("kodepinjam"),
                    rs.getString("kodebuku"),
                    rs.getString("statuspinjam"),
                    rs.getDate("tglpinjam").toString(),
                    rs.getDate("tglkembali").toString()
                };

                model.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }
