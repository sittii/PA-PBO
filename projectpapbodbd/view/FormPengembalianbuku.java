    private void tablebukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablebukuMouseClicked
        int selectedRow = tablebuku.getSelectedRow(); // Dapatkan indeks baris yang diklik

        if (selectedRow >= 0) {
        String selectedKodepinjam = (String) tablebuku.getValueAt(selectedRow, 1); 
        String selectedKodebuku = (String) tablebuku.getValueAt(selectedRow, 2);
        String selectedStatuspinjam = (String) tablebuku.getValueAt(selectedRow, 3);
        String selectedTglpinjam = (String) tablebuku.getValueAt(selectedRow, 4);
        String selectedTglkembali = (String) tablebuku.getValueAt(selectedRow, 5);
       
        txtkodepinjam.setText(selectedKodepinjam);
        txtkodebuku.setText(selectedKodebuku);
        txtstatus.setText(selectedStatuspinjam);
        txttglpinjam.setText(selectedTglpinjam);
        txttglkembali.setText(selectedTglkembali);
        }
    }//GEN-LAST:event_tablebukuMouseClicked

