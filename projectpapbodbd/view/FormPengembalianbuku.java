    private void btnpengembalianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpengembalianActionPerformed
        String kodepinjam = txtkodepinjam.getText();
        String statuspinjam = txtstatus.getText();
        String kodebuku = txtkodebuku.getText();

        Pengembalianbuku bukukembali = new Pengembalianbuku(this.kodeuser, kodepinjam, kodebuku);

        boolean updated = bukukembali.simpanPengembalian();

        if (updated) {
            JOptionPane.showMessageDialog(this, "Pengembalian buku berhasil", "Berhasil", JOptionPane.INFORMATION_MESSAGE);
            txtkodepinjam.setText("");
            txtstatus.setText("");
            txtkodebuku.setText("");
            txttglpinjam.setText("");
            txttglkembali.setText("");
            model.setRowCount(0); 
            showData();
        } else {
            JOptionPane.showMessageDialog(null, "Anda terlambat mengembalikan buku, terkena denda Rp 10.000, silahkan datangi petugas untuk melakukan pembayaran denda", "Gagal", JOptionPane.ERROR_MESSAGE);
            txtkodepinjam.setText("");
            txtstatus.setText("");
            txtkodebuku.setText("");
            txttglpinjam.setText("");
            txttglkembali.setText("");
        }
    }//GEN-LAST:event_btnpengembalianActionPerformed

