    private void btnkonfirmasiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnkonfirmasiActionPerformed
        String kodedenda = txtkodedenda.getText();
        String kodeuseranggota = txtpeminjam.getText();
        String kodepinjam = txtkodepinjam.getText(); // Perbaiki ini menjadi txtkodebuku
        String kodebuku = txtkodebuku.getText();

        Denda denda = new Denda(kodedenda, kodepinjam, kodebuku, this.kodeuser, kodeuseranggota);
        boolean result = denda.simpanDenda();

        if (result) {
            JOptionPane.showMessageDialog(this, "Konfirmasi berhasil.", "Konfirmasi", JOptionPane.INFORMATION_MESSAGE);
            txtkodedenda.setText("");
            txtpeminjam.setText("");
            txtkodepinjam.setText("");
            txtkodebuku.setText("");
            model.setRowCount(0); // Hapus semua baris dalam tabel
            showData();
        } else {
            JOptionPane.showMessageDialog(this, "Konfirmasi gagal.", "Konfirmasi", JOptionPane.ERROR_MESSAGE);txtkodedenda.setText("");
            txtkodedenda.setText("");
            txtpeminjam.setText("");
            txtkodepinjam.setText("");
            txtkodebuku.setText("");
            model.setRowCount(0); // Hapus semua baris dalam tabel
            showData();
        }        
    }//GEN-LAST:event_btnkonfirmasiActionPerformed

    private void tabledendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabledendaMouseClicked
        int selectedRow = tabledenda.getSelectedRow();

        if (selectedRow >= 0) {
            String selectedKodedenda = (String) tabledenda.getValueAt(selectedRow, 1);
            String selectedKodepinjam = (String) tabledenda.getValueAt(selectedRow, 3);
            String selectedKodebuku = (String) tabledenda.getValueAt(selectedRow, 4);
            String selectedKodeuser = (String) tabledenda.getValueAt(selectedRow, 5);

            txtkodedenda.setText(selectedKodedenda);
            txtkodepinjam.setText(selectedKodepinjam);
            txtkodebuku.setText(selectedKodebuku);
            txtpeminjam.setText(selectedKodeuser);
        }
    }//GEN-LAST:event_tabledendaMouseClicked

