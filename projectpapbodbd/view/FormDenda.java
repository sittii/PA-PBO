    private void showData() {
    try {
        st = conn.createStatement();
        rs = st.executeQuery("SELECT d.kodedenda, d.jumlahdenda, d.peminjamanbuku_kodepinjam, p.buku_kodebuku, p.anggota_kodeuser FROM denda d JOIN peminjamanbuku p ON p.kodepinjam = d.peminjamanbuku_kodepinjam WHERE keterangan is null");

        int no = 1;

        while (rs.next()) {
            Object[] data = {
                no++,
                rs.getString("kodedenda"),
                rs.getString("jumlahdenda"),
                rs.getString("peminjamanbuku_kodepinjam"),
                rs.getString("buku_kodebuku"),
                rs.getString("anggota_kodeuser")
            };

            model.addRow(data);
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
    }
}

