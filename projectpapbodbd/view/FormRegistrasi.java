    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nama = txtnama.getText();
        String username = txtusername.getText();
        String password = txtpassword.getText();
        String role = "Anggota";
        String alamatEmail = txtemail.getText();

        User newUser = new User(nama, username, password, role);
        Anggota newAnggota = new Anggota(newUser.getKodeUser(), alamatEmail);

        String nextUserCode = newUser.getKodeUser();
        String nextAnggotaCode = newAnggota.getKodeUser();

        if (nextUserCode != null) {
            boolean isSuccess = newUser.tambah();
            boolean isAnggotaSuccess = newAnggota.tambah();

            if (isSuccess && isAnggotaSuccess) {
                 JOptionPane.showMessageDialog(null, "Berhasil membuat akun");
                 this.dispose();
                 FormLogin login = new FormLogin();
                 login.setVisible(true);
            } else {
                 JOptionPane.showMessageDialog(null, "Gagal membuat akun");
                 txtnama.setText("");
                 txtusername.setText("");
                 txtpassword.setText("");
                 txtemail.setText("");
            }
        } else {
             JOptionPane.showMessageDialog(null, "Gagal mendapatkan kodeuser");
                 txtnama.setText("");
                 txtusername.setText("");
                 txtpassword.setText("");
                 txtemail.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

